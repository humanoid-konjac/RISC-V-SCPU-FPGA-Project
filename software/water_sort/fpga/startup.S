    .section .text.start
    .globl _start
    .type _start, @function

_start:
    la sp, __stack_top
    la t0, __bss_start
    la t1, __bss_end

.Lbss_loop:
    bgeu t0, t1, .Lbss_done
    sw zero, 0(t0)
    addi t0, t0, 4
    j .Lbss_loop

.Lbss_done:
    call isa_coverage
    call main

.Lhalt:
    j .Lhalt

    .size _start, .-_start

    .align 2
    .globl enable_keyboard_interrupts
    .type enable_keyboard_interrupts, @function
enable_keyboard_interrupts:
    la t0, trap_entry
    .word 0x30529073              # csrw mtvec,t0
    addi t0, zero, 128
    .word 0x3042a073              # csrs mie,t0 (MTIE)
    addi t0, zero, 8
    .word 0x3002a073              # csrs mstatus,t0 (MIE)
    jalr zero, 0(ra)
    .size enable_keyboard_interrupts, .-enable_keyboard_interrupts

    .align 2
    .globl trap_entry
    .type trap_entry, @function
trap_entry:
    addi sp, sp, -128
    sw ra,   0(sp)
    sw gp,   4(sp)
    sw tp,   8(sp)
    sw t0,  12(sp)
    sw t1,  16(sp)
    sw t2,  20(sp)
    sw s0,  24(sp)
    sw s1,  28(sp)
    sw a0,  32(sp)
    sw a1,  36(sp)
    sw a2,  40(sp)
    sw a3,  44(sp)
    sw a4,  48(sp)
    sw a5,  52(sp)
    sw a6,  56(sp)
    sw a7,  60(sp)
    sw s2,  64(sp)
    sw s3,  68(sp)
    sw s4,  72(sp)
    sw s5,  76(sp)
    sw s6,  80(sp)
    sw s7,  84(sp)
    sw s8,  88(sp)
    sw s9,  92(sp)
    sw s10, 96(sp)
    sw s11,100(sp)
    sw t3, 104(sp)
    sw t4, 108(sp)
    sw t5, 112(sp)
    sw t6, 116(sp)

    call keyboard_interrupt_handler

    lw ra,   0(sp)
    lw gp,   4(sp)
    lw tp,   8(sp)
    lw t0,  12(sp)
    lw t1,  16(sp)
    lw t2,  20(sp)
    lw s0,  24(sp)
    lw s1,  28(sp)
    lw a0,  32(sp)
    lw a1,  36(sp)
    lw a2,  40(sp)
    lw a3,  44(sp)
    lw a4,  48(sp)
    lw a5,  52(sp)
    lw a6,  56(sp)
